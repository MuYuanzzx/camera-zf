#define oled_scl_pin OLED_I2C_SCL_PIN
#define oled_sda_pin OLED_I2C_SDA_PIN

/*********************************************************************************/
#define i2c_read_sda() gpio_get_level(oled_sda_pin)
#define i2c_sda_high() gpio_high(oled_sda_pin)
#define i2c_sda_low() gpio_low(oled_sda_pin)
#define i2c_scl_high() gpio_high(oled_scl_pin)
#define i2c_scl_low() gpio_low(oled_scl_pin)

void bsp_analog_i2c_start(void);
void bsp_analog_i2c_stop(void);

static void analog_i2c_delay(void)
{
  volatile uint8_t i;
  for (i = 0; i < 10; i++)
    ;
}

void bsp_analog_i2c_init(void)
{
  gpio_init(oled_scl_pin, GPO, GPIO_HIGH, GPO_PUSH_PULL); // 提取对应IO索引 AF功能编码
  gpio_init(oled_sda_pin, GPO, GPIO_HIGH, GPO_PUSH_PULL); // 提取对应IO索引 AF功能编码
}

void i2c_sda_out(void)
{
  gpio_set_dir(oled_sda_pin, GPO, GPO_PUSH_PULL);
}
void i2c_sda_in(void)
{
  gpio_set_dir(oled_sda_pin, GPI, GPI_FLOATING_IN);
}

void bsp_analog_i2c_start(void)
{
  /*    _____
   *SDA      \_____________
   *    __________
   *SCL           \________
   */
  i2c_sda_out();
  i2c_sda_high();
  i2c_scl_high();
  analog_i2c_delay();
  i2c_sda_low();
  analog_i2c_delay();
  i2c_scl_low();
  analog_i2c_delay();
}

void bsp_analog_i2c_stop(void)
{
  /*               _______
   *SDA __________/
   *          ____________
   *SCL _____/
   */
  i2c_sda_out();
  i2c_sda_low();
  i2c_scl_high();
  analog_i2c_delay();
  i2c_sda_high();
  analog_i2c_delay();
}

uint8_t bsp_analog_i2c_wait_ack(void)
{
  uint32_t timeout = 0;
  i2c_sda_high();
  i2c_sda_in();
  analog_i2c_delay();
  i2c_scl_high();
  analog_i2c_delay();
  while (i2c_read_sda())
  {
    timeout++;
    if (timeout > 100) // 2000
    {
      bsp_analog_i2c_stop();
      return 1;
    }
  }
  i2c_scl_low();
  analog_i2c_delay();
  return 0;
}

void bsp_analog_i2c_ack(void)
{
  /*           ____
   *SCL ______/    \______
   *    ____         _____
   *SDA     \_______/
   */
  i2c_scl_low();
  i2c_sda_out();
  i2c_sda_high();
  analog_i2c_delay();
  i2c_sda_low();
  analog_i2c_delay();
  i2c_scl_high();
  analog_i2c_delay();
  i2c_scl_low();
  analog_i2c_delay();
  i2c_sda_high();
}

void bsp_analog_i2c_nack(void)
{
  /*           ____
   *SCL ______/    \______
   *    __________________
   *SDA
   */
  i2c_scl_low();
  i2c_sda_out();
  i2c_sda_high();
  analog_i2c_delay();
  i2c_scl_high();
  analog_i2c_delay();
  i2c_scl_low();
  analog_i2c_delay();
}

void bsp_analog_i2c_send_byte(uint8_t data)
{
  uint8_t i;
  i2c_sda_out();
  i2c_scl_low();
  for (i = 0; i < 8; i++)
  {
    if ((data & 0x80) >> 7)
    {
      i2c_sda_high();
    }
    else
    {
      i2c_sda_low();
    }
    data <<= 1;
    analog_i2c_delay();
    i2c_scl_high();
    analog_i2c_delay();
    i2c_scl_low();
    analog_i2c_delay();
  }
  bsp_analog_i2c_wait_ack();
}

uint8_t bsp_analog_i2c_read_byte(void)
{
  uint8_t i, data = 0;
  for (i = 0; i < 8; i++)
  {
    data <<= 1;
    i2c_scl_high();
    analog_i2c_delay();
    if (i2c_read_sda())
    {
      data++;
    }
    i2c_scl_low();
    analog_i2c_delay();
  }
  return data;
}

/*********************OLED写数据************************************/
void OLED_WrDat(unsigned char IIC_Data)
{
#if oled_drive_i2c_enable
  bsp_analog_i2c_start();
  bsp_analog_i2c_send_byte(0x78);
  bsp_analog_i2c_send_byte(0x40); // write data
  bsp_analog_i2c_send_byte(IIC_Data);
  bsp_analog_i2c_stop();
#else
  unsigned char i = 8;
  LCD_DCout(BIT_SET);
  for (i = 0; i < 8; i++) // 传送8位数据：时钟线拉低有效
  {
    LCD_SCLout(BIT_RESET);
    if (IIC_Data & 0x80)
      LCD_SDAout(BIT_SET);
    else
      LCD_SDAout(BIT_RESET);
    LCD_SCLout(BIT_SET);
    IIC_Data <<= 1;
  }
#endif
}
/*********************OLED写命令************************************/
void OLED_WrCmd(unsigned char IIC_Command)
{
#if oled_drive_i2c_enable
  bsp_analog_i2c_start();
  bsp_analog_i2c_send_byte(0x78); // Slave address,SA0=0
  bsp_analog_i2c_send_byte(0x00); // write command
  bsp_analog_i2c_send_byte(IIC_Command);
  bsp_analog_i2c_stop();
#else
  unsigned char i = 8;
  LCD_DCout(BIT_RESET);
  for (i = 0; i < 8; i++) // 传送8位数据：时钟线拉低有效
  {
    LCD_SCLout(BIT_RESET);
    if (IIC_Command & 0x80)
      LCD_SDAout(BIT_SET);
    else
      LCD_SDAout(BIT_RESET);
    LCD_SCLout(BIT_SET);
    IIC_Command <<= 1;
  }
  LCD_DCout(BIT_SET);
#endif
}

/*********************OLED 设置坐标************************************/
void OLED_Set_Pos(unsigned char x, unsigned char y)
{
  OLED_WrCmd(0xb0 + y);
  OLED_WrCmd(((x & 0xf0) >> 4) | 0x10);
  OLED_WrCmd((x & 0x0f) | 0x01);
}
/*********************OLED全屏************************************/
void OLED_Fill(unsigned char bmp_dat)
{
  unsigned char y, x;
  for (y = 0; y < 8; y++)
  {
    OLED_WrCmd(0xb0 + y);
    OLED_WrCmd(0x01);
    OLED_WrCmd(0x10);
    for (x = 0; x < X_WIDTH; x++)
      OLED_WrDat(bmp_dat);
  }
}

/*********************OLED复位************************************/
void OLED_CLS(void)
{
  unsigned char y, x;
  for (y = 0; y < 8; y++)
  {
    OLED_WrCmd(0xb0 + y);
    OLED_WrCmd(0x01);
    OLED_WrCmd(0x10);
    for (x = 0; x < X_WIDTH; x++)
      OLED_WrDat(0);
  }
}
/************************************************************************/
void LCD_P6x8Str(unsigned char x, unsigned char y, unsigned char ch[])
{
  unsigned char c = 0, i = 0, j = 0;
  while (ch[j] != '\0')
  {
    c = ch[j] - 32;
    if (x > 126)
    {
      x = 0;
      y++;
    }
    OLED_Set_Pos(x, y);
    for (i = 0; i < 6; i++)
      OLED_WrDat(F6x8[c][i]);
    x += 6;
    j++;
  }
}

// 显示一个6X8的字符
void LCD_P6x8Char(unsigned char x, unsigned char y, unsigned char ucData)
{
  unsigned char i, ucDataTmp;
  ucDataTmp = ucData - 32;
  if (x > 126)
  {
    x = 0;
    y++;
  }
  OLED_Set_Pos(x, y);
  for (i = 0; i < 6; i++)
  {
    OLED_WrDat(F6x8[ucDataTmp][i]);
  }
}

/*--------------显示6X8的浮点数--------------*/
void write_6_8_number(unsigned char x, unsigned char y, float number)
{
  unsigned char i = 0;
  unsigned char temp[16];
  unsigned char *point = temp;
  float decimal;
  int data;
  if (number < 0)
  {
    temp[0] = '-';
    LCD_P6x8Char(x, y, temp[0]);
    x += 6;
    number = -number;
  }
  data = (int)number;
  decimal = number - data; // 小数部分

  if (data >= 1000000000) // 是否能被10^9整除
  {
    temp[i] = 48 + data / 1000000000;
    data = data % 1000000000;
    i++;
  }
  if (data >= 100000000) // 是否能被10^8整除
  {
    temp[i] = 48 + data / 100000000;
    data = data % 100000000;
    i++;
  }
  else if (data < 100000000 && i != 0)
  {
    temp[i] = 0 + 48;
    i++;
  }
  if (data >= 10000000) // 是否能被10^7整除
  {
    temp[i] = 48 + data / 10000000;
    data = data % 10000000;
    i++;
  }
  else if (data < 10000000 && i != 0)
  {
    temp[i] = 0 + 48;
    i++;
  }
  if (data >= 1000000) // 是否能被10^6整除
  {
    temp[i] = 48 + data / 1000000;
    data = data % 1000000;
    i++;
  }
  else if (data < 1000000 && i != 0)
  {
    temp[i] = 0 + 48;
    i++;
  }
  if (data >= 100000) // 是否能被100000整除
  {
    temp[i] = 48 + data / 100000;
    data = data % 100000;
    i++;
  }
  else if (data < 100000 && i != 0)
  {
    temp[i] = 0 + 48;
    i++;
  }
  if (data >= 10000) // 是否能被10000整除
  {
    temp[i] = 48 + data / 10000;
    data = data % 10000;
    i++;
  }
  else if (data < 10000 && i != 0)
  {
    temp[i] = 0 + 48;
    i++;
  }
  if (data >= 1000) // 是否能被1000整除
  {
    temp[i] = 48 + data / 1000;
    data = data % 1000;
    i++;
  }
  else if (data < 1000 && i != 0)
  {
    temp[i] = 0 + 48;
    i++;
  }
  if (data >= 100) // 是否能被100整除
  {
    temp[i] = 48 + data / 100;
    data = data % 100;
    i++;
  }
  else if (data < 100 && i != 0)
  {
    temp[i] = 0 + 48;
    i++;
  }
  if (data >= 10) // 是否能被10整除
  {
    temp[i] = 48 + data / 10;
    data = data % 10;
    i++;
  }
  else if (data < 10 && i != 0)
  {
    temp[i] = 48;
    i++;
  }
  temp[i] = 48 + data;
  if (decimal >= 0.0001f) // 判断是否有小数部分
  {
    i++;
    temp[i] = '.'; // 显示小数点
    i++;
    data = (int)(decimal * 1000);
    temp[i] = 48 + data / 100;
    data = data % 100;
    i++;
    if (data > 0)
    {
      temp[i] = 48 + data / 10;
      data = data % 10;
    }
    if (data >= 0)
    {
      i++;
      temp[i] = data + 48;
    }
  }
  i++;
  temp[i] = '\0';
  LCD_P6x8Str(x, y, point);
}

